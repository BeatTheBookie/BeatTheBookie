

/**********************************************
	Load stage table for historic
	data of Bundesliga
	
select
	column_name || ','
from
	EXA_ALL_COLUMNS
where
	column_schema =  'SOURCE_FILES' and
	column_table = 'GER_D1_2010_11'
order by
	column_ordinal_position
;

select
	case
		when column_type like 'VARCHAR%' then column_name || ','
		when column_type = 'DATE' then 'to_date('|| column_name || ',''dd/mm/yy''),'
		when column_type like 'DECIMAL%' then 'to_number(' || column_name || ',''999.999''),'
	end
from
	EXA_ALL_COLUMNS stg
where
	column_schema =  'STAGE' and
	column_table = 'football_his' and
	exists (select
				column_name
			from
				EXA_ALL_COLUMNS src
			where
				src.column_name = stg.column_name and
				column_schema =  'SOURCE_FILES' and
				column_table = 'GER_D1_2015_16'
			)			
order by
	column_ordinal_position
;

**********************************************/


